#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*n_s;

	n_s = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		n_s[i] = '\0';
		i++;
	}
}
